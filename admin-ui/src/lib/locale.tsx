const LOCALE_LABEL: Record<AdminLocale, string> = {
  en: 'English',
  vi: 'Tieng Viet',
}

export function localeToIntl(locale: AdminLocale): string {
  return locale === 'vi' ? 'vi-VN' : 'en-US'
}

function translate(locale: AdminLocale, key: TranslationKey, params?: Record<string, string | number>): string {
  const template = MESSAGES[locale][key] ?? MESSAGES.en[key]
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, token: string) => {
    const value = params[token]
    return value === undefined ? `{${token}}` : String(value)
  })
}

const LocaleContext = createContext<LocaleContextValue | null>(null)

export function LocaleProvider({
  locale,
  setLocale,
  children,
}: {
  locale: AdminLocale
  setLocale: (locale: AdminLocale) => void
  children: ReactNode
}) {
  const value: LocaleContextValue = {
    locale,
    setLocale,
    t: (key, params) => translate(locale, key, params),
    localeLabel: LOCALE_LABEL[locale],
    formatNumber: (num, options) => new Intl.NumberFormat(localeToIntl(locale), options).format(num),
    formatDateTime: (date, options) => {
      const value = typeof date === 'number' ? new Date(date) : date
      return value.toLocaleString(localeToIntl(locale), options)
    },
  }

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
}

export function useLocale() {
  const context = useContext(LocaleContext)
  if (!context) {
    throw new Error('useLocale must be used within LocaleProvider')
  }
  return context
}
